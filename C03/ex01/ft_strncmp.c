int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	b;
	char			a;

	b = 0;
	a = 0;
	while ((s1[b] != '\0' || s2[b] != '\0') && (b < n))
	{
		a = s1[b] - s2[b];
		if (s1[b] != s2[b])
			return (a);
		b++;
	}
	return (a);
}
