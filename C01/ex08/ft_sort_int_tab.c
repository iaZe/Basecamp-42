void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	i;

	i = 0;
	while (i < size - 1)
	{
		a = tab[i];
		tab[i] = tab[i + 1];
		tab[i + 1] = a;
		if (tab[i + 1] < tab[i])
		{
			i++;
		}
	}
	i = 0;
}
